<?php
/*
 * Task 72. Возводим в степень
 *
 * Задача: Вычислить значение a^b mod m,
 * где 1 <= a <= 10^9, 1 <= b <= 10^7, 2 <= m <= 10^9
 *
 * Пример: a 595, b = 703, m = 991 a^b mod m = 342.
 *
 * Идея: воспользуемся свойством числа по модулю
 */

list ($b, $n, $m) = [595, 703, 991];
$c = 1; $nn = 0;
while ($nn < $n) {
  $nn++;
  $c = ($b * $c) % $m;
}
echo $c;